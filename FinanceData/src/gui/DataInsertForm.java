    private void initTitleFinanceName() {
//          INSERIRE CASO IN CUI NON C'E' PIU' IL FILE!!        
        BufferedReader buffer = null;
        try {
            buffer = new BufferedReader(new FileReader(setup.getDatabaseAbsolutePath()));
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DataInsertForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        this.financeTitleBox.addItem('-');
        
        String line = null;
        JSONObject obj = null;
        while (true) {
            try {
                line = buffer.readLine();
            } catch (IOException ex) {
                Logger.getLogger(DataInsertForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            if (line != null) {
                try {
                    obj = new JSONObject(line);
                    this.financeTitleBox.addItem(obj.getString(DataInserert.NAME_KEY));
                } catch (JSONException ex) {
                    Logger.getLogger(DataInsertForm.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                break;
            }            
        }        
    }
    
